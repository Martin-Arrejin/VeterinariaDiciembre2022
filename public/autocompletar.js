let especie = document.getElementById("especie");
let seleccionEspecie;

especie.addEventListener("change", () => {
    seleccionEspecie = especie.value;
});

let input = document.getElementById("input");

input.addEventListener("keyup", (e) => {
    //El evento onKeyUp tiene lugar cuando el usuario deja de pulsar una tecla. El evento onKeyPress se genera cuando se mantiene pulsada una tecla.
    removeElements();
    switch (seleccionEspecie) {
        case "Perro": {
            buscador(ordenarPerros);
            break;
        }
        case "Gato": {
            buscador(ordenarGatos);
            break;
        }
        case "Pajaro": {
            buscador(ordenarPajaros);
            break;
        }
        case "Conejo": {
            buscador(ordenarConejos);
            break;
        }
        case "Tortuga": {
            buscador(ordenarTortugas);
            break;
        }
        default:
            //ninguno de los valores coincide con el valor de la expresión
            console.log("no hay valores encontrados");
            break;
    }
});
function displayNames(value) {
    input.value = value;
    removeElements();
}
function removeElements() {
    let items = document.querySelectorAll(".list-items");
    items.forEach((item) => {
        item.remove();
    });
}

function buscador(raza) {
    for (let i of raza) {
        //convert input to lowercase and compare with each string

        if (
            i.toLowerCase().startsWith(input.value.toLowerCase()) &&
            input.value != ""
        ) {
            //crear un elemento li
            let listItem = document.createElement("li");
            //One common class name
            listItem.classList.add("list-items");
            listItem.style.cursor = "pointer";
            listItem.setAttribute("onclick", "displayNames('" + i + "')");
            //Display matched part in bold
            let word = "<b>" + i.substr(0, input.value.length) + "</b>";
            word += i.substr(input.value.length);
            //display the value in array
            listItem.innerHTML = word;
            document.querySelector(".list").appendChild(listItem);
        }
    }
}
